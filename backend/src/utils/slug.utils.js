// Convierte un nombre en un slug URL-friendly
// "Camiseta Azul Marino" → "camiseta-azul-marino"
export const generateSlug = (text) => {
  return text
    .toLowerCase()
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .replace(/[^a-z0-9\s-]/g, '')
    .trim()
    .replace(/\s+/g, '-')
}